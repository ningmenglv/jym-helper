<script>
import nprogress from 'nprogress'
export default {
  name: 'SecurityRoute',
  data() {
    return {
      animationMoudle: []
    }
  },
  beforeCreate() {
    this.$store.commit('header/setNavActive', true)
  },
  created() {
    document.getElementsByTagName('body')[0].className += 'snow-container'
  },
  methods: {
    init() {
      let insuranceDom1 = document.getElementsByClassName('insurance2')[0]
      setTimeout(() => {
        insuranceDom1.className = 'insurance active insurance2'
      }, 300)
    },
    animate(ref, minheight, maxheight) {
      let bodyTop = window.pageYOffset || window.document.documentElement.scrollTop || document.body.scrollTop
      let { animationMoudle, clientHeight } = this.$data
      animationMoudle.forEach((item, index) => {
        if (bodyTop + clientHeight / 2 > item.top && bodyTop + clientHeight / 2 < item.bottom) {
          item.dom.className = 'insurance active' + ' insurance' + item.classIndex
          animationMoudle.splice(index, 1)
          if (animationMoudle.length === 0) {
            window.removeEventListener('scroll', this.animate)
          }
        }
      })
    },
    handlerAnimationMoudle() {
      let insuranceDom = document.querySelectorAll('.insurance')
      let animationMoudle = []
      let topConfig = [530, 800]
      for (let i = 0; i < 2; i++) {
        let top = topConfig[i] // insuranceDom[i].offsetTop
        let height = insuranceDom[i].offsetHeight
        animationMoudle.push({
          top: top,
          bottom: top + height,
          dom: insuranceDom[i],
          classIndex: i + 2
        })
      }
      this.$data.animationMoudle = animationMoudle
      this.$data.clientHeight = document.documentElement.clientHeight
      window.addEventListener('scroll', this.animate)
    }
  },
  mounted() {
    this.init() // 首次进入，第一个动画
    this.handlerAnimationMoudle()
    nprogress.done()
  },
  beforeDestroy() {
    window.removeEventListener('scroll', this.animate)
  }
}
</script>

